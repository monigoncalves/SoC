#Notar que: (A) Esquerda: multiplicar por potências de 2; 
#(B) Direita: dividir por potências de 2. 
# Ex: 1<<3 ou 1*8; 2>>2 ou 2/4
#Sintaxe Lógico: >>>
#Sintaxe Aritmetico: >>  

# Left Shift
print(1<<9)

# Logical Right Shift
#print(1>>>9)

#Arithmetic Right Shift
